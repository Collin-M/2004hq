<center><b>Pottery</b><br><br></center>
<table border="0"><tr><td>
Bowls, pots and pie dishes can all be made by crafting. 
Clay is obtained by <a href="https://web.archive.org/web/20050204054353/http://www.runescape.com/aff/runescape/skills/mining.html" class="c">mining</a>.<br>
To make the clay workable it needs to be softened first.
Use a jug of water with the clay and it will be turned into soft clay.
Take the clay to a potter's wheel. There is one in the barbarian village.<br>
Use the clay on the potter's wheel and select what object you would like to make. If you have the correct crafting level you will make an unfired piece of pottery. If you want to make several of the same object, simply right-click and select the number you wish to produce.
Use it on the pottery oven and as long as it doesn't crack upon heating, you will have made yourself a nice new piece of pottery.
</td><td><img src="https://web.archive.org/web/20050204054353im_/http://www.runescape.com/img/rs2/manual/crafting/pottery.gif" width="159" height="151"></td></tr></table>

<p>
<center><b>Leather</b></center><br>
Leather is made from cow hides, so you will need to find a cow field and kill some cows.
<table><tr><td valign="top"><img src="https://web.archive.org/web/20050204054353im_/http://www.runescape.com/img/rs2/manual/crafting/leather.gif"></td><td>
Once you have a cow hide, take it to the tannery in Al Kharid. You can then craft your leather into leather armour, boots or gloves. You will need to get a needle and some thread from a crafting shop. Right-click the needle in your inventory and use it on a piece of leather, then as long as you have some thread the screen below will be opened, and you will be given a choice of leather objects to make.
</td></tr></table><br>
Simply click on the item you want to make and if you have enough thread you will craft it. If you want to make several of the same object, simply right-click and select the number you wish to produce.
<p><center>
<img src="https://web.archive.org/web/20050204054353im_/http://www.runescape.com/img/rs2/manual/crafting/leather.jpg"></center><p>
There are 3 types of leather - normal, hard and dragon. You are given the choice of either normal or hard leather when you get your cow hides converted by the tanner. Expect to pay a little more for hardened leather.
Dragon leather is made in the same way as normal leather, except dragon hides are used instead of cow hides.
<p>
Kill a dragon to collect it's hide, then speak to a tanner in the same way, and you get the option to make
dragon leather if you have any dragonhides in your inventory. The cost for converting dragonhides is more
expensive than hardened leather however. Use a needle and thread again and a smaller crafting display is shown with the items that you can craft from dragon leather.
<p>
Adding studs to a leather body or chaps increases it's strength. Studs can be smithed from steel, then used  with the leather item to make it studded. Studs can only be used on normal leather.
<p>
<center><b>Gems</b></center><br>
Gems are rare objects you will occasionally find, they can be made more valuable by cutting them. Once a gem is cut it can also be used when you are making gold jewelry.<br>
You will need to buy a chisel from a general store or a crafting shop.
Select the chisel from your inventory and then the gem you wish to cut.
